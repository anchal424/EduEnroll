package com.eduenroll.beans;

public class UserAnalyticsBean {
    private String label;
    private int count;

    public UserAnalyticsBean() {}

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}